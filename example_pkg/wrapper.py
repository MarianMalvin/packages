def arrows_wrapper(string):
    print("↧" * len(string))
    print(string)
    print("↥" * len(string))


def stars_wrapper(string):
    print("*" * len(string))
    print(string)
    print("*" * len(string))


def lines_wrapper(string):
    print("-" * len(string))
    print(string)
    print("-" * len(string))


def hashtags_wrapper(string):
    print("#" * len(string))
    print(string)
    print("#" * len(string))


def tildes_wrapper(string):
    print("~" * len(string))
    print(string)
    print("~" * len(string))
